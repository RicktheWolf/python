
from math import *

print (max(3,5,1,9))       # maximum
print (min(3,5,1,9))       # minimum
print (abs(-5))             # absolute number (the distance of given number from 0)
print (pow(5,3))           # power like 3^5
print (sqrt(25))           # square root
print (floor(3.5))         # floor number
print (ceil(3.5))          # ceiling number
print (round(3.6))         # round number

print(radians(180))
print(tan(120))